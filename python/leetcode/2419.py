from typing import List


class Solution:
    def longestSubarray(self, nums: List[int]) -> int:
        max_ = max(nums)
        res = -1
        curr_count = 0
        
        for n in nums:
            if n == max_:
                curr_count += 1
                res = max(res, curr_count)
            else:
                curr_count = 0
        return res
            
